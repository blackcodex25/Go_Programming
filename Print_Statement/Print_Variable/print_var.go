package main

import "fmt"

// ในตัวอย่างนี้เราใช้ fmt.Print() เพื่อพิมพ์ค่าของตัวแปรใน ฌน

func main() {
	name := "Jhon"
	fmt.Print(name)
}

/* โปรแกรมนี้จะพิมพ์ค่าของตัวแปร name ซึ่งในที่นี้คือ "Jhon" */
/* หมายเหตุ: เราไม่ควรใส่ตัวแปรไว้ในเครื่องหมายคำพูดเมื่อพิมพ์ค่า */
/* เพราะจะถือว่าเป็นสตริง (ข้อความ) แทนที่จะเป็นค่าของตัวแปร */
