package main

import "fmt"

/* ใน Go เราสามารถพิมพ์ค่าหลายค่าพร้อมกันได้ โดยการแยกค่าด้วย */
/* เครื่องหมายจุลภาค (,) */

// โปรแกรมแสดงการใช้ fmt.Print()
func main() {
	name := "Jhon"            // ค่าของตัวแปร name
	fmt.Print("Name: ", name) // พิมพ์ค่าของตัวแปร name
}

/* ในตัวอย่างนี้ ฟังก์ชัน fmt.Print() ถูกใช้เพื่อพิมพ์ข้อความ "Name: " */
/* และค่าของตัวแปร name ("Jhon") พร้อมกัน */
