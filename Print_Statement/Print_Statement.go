package main

import "fmt" // นำเข้าแพ็กเกจ fmt

// โปรแกรมตัวอย่างเพื่อแสดงการใช้ fmt.Print()

func main() {
	fmt.Print("Hello, ")
	fmt.Print("World!\n")
}

/* ในโปรแกรมนี้ fmt.Println() จะใช้ในการพิมพ์ข้อความ "Hello, " และ */
/* "World!" โดยที่ข้อความจะไม่ขึ้นบรรทัดใหม่ ดังนั้นผลลัพธ์ที่ได้คือ */
/* ข้อความต่อกันในบรรทัดเดียว */
