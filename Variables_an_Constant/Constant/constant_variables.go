package main

import "fmt"

/* ค่าคงที่ Constants คือค่าที่ไม่สามารถเปลี่ยนแปลงได้ */
/* หลังจากประกาศ โดยใช้คำสั่ง const เพื่อสร้างตัวแปรคงที่ เช่น */

const lightSpeed = 299792458 // ค่าเริ่มต้น

func main() {
	//  หากพยายามเปลี่ยนแปลงค่าคงที่ เช่น
	//  lightSpeed = 299792460 // จะเกิดข้อผิดพลาด

	// นอกจากนี้ไม่สามารถใช้รูปแบบย่อ := ในการสร้างค่าคงที่ได้
	// const lightSpeed2 := 299792458 // จะเกิดข้อผิดพลาด

	fmt.Println(lightSpeed)
}
