package main

import "fmt"

func main() {
	// ประกาศตัวแปร boolValue ประเภท Boolean
	var boolValue bool
	// กำหนดค่าเริ่มต้นเป็น false
	boolValue = false
	// พิมพ์ค่า boolValue
	fmt.Println(boolValue)
}

/* คำอธิบาย: ในโปรแกรมนี้ เราได้สร้างตัวแปร boolValue ประเภท */
/* Boolean และกำหนดค่าเริ่มต้นเป็น false แล้วใช้ฟังก์ชัน */
/* fmt.Println() เพื่อพิมพ์ค่า boolValue */
